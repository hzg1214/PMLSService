package cn.com.eju.pmls.channelBusiness.model;

import java.util.ArrayList;
import java.util.List;

public class ChannelBrandDto {
    private Integer id;//主键ID
    private String brandCode;//品牌编号
    private String brandName;//品牌名称
    private Integer parentId;//父级ID
    private String orgId;//架构ID
    private String dateCreate;//创建时间
    private String userIdCreate;//创建人
    private String createUserName;//创建人姓名
    private Integer delFlag;//删除标识
    private String remark;//备注

    private boolean spread=true;//默认展开
    private String name;
    private List<ChannelBrandDto> children=new ArrayList<ChannelBrandDto>();

    public boolean isSpread() {
        return spread;
    }

    public void setSpread(boolean spread) {
        this.spread = spread;
    }

    public List<ChannelBrandDto> getChildren() {
        return children;
    }

    public void setChildren(List<ChannelBrandDto> children) {
        this.children = children;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCreateUserName() {
        return createUserName;
    }

    public void setCreateUserName(String createUserName) {
        this.createUserName = createUserName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getBrandCode() {
        return brandCode;
    }

    public void setBrandCode(String brandCode) {
        this.brandCode = brandCode;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public Integer getParentId() {
        return parentId;
    }

    public void setParentId(Integer parentId) {
        this.parentId = parentId;
    }

    public String getOrgId() {
        return orgId;
    }

    public void setOrgId(String orgId) {
        this.orgId = orgId;
    }

    public String getDateCreate() {
        return dateCreate;
    }

    public void setDateCreate(String dateCreate) {
        this.dateCreate = dateCreate;
    }

    public String getUserIdCreate() {
        return userIdCreate;
    }

    public void setUserIdCreate(String userIdCreate) {
        this.userIdCreate = userIdCreate;
    }

    public Integer getDelFlag() {
        return delFlag;
    }

    public void setDelFlag(Integer delFlag) {
        this.delFlag = delFlag;
    }
}
