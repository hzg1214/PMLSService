package cn.com.eju.deal.user.dto;

import java.io.Serializable;
import java.util.List;

public class GroupDto implements Serializable{

	private static final long serialVersionUID = 1L;

	private Integer id;

    private String text;               
 
    private String typeName;               
    
    private List<GroupDto> children;
    
    private String orgIdStr;

    public String getOrgIdStr()
    {
        return orgIdStr;
    }

    public void setOrgIdStr(String orgIdStr)
    {
        this.orgIdStr = orgIdStr;
    }
	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public List<GroupDto> getChildren() {
		return children;
	}

	public void setChildren(List<GroupDto> children) {
		this.children = children;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
    
    

}
