package cn.com.eju.deal.user.model;

import cn.com.eju.deal.core.model.BaseModel;

public class UserOrderCount extends BaseModel
{
    private Integer newNum;
    
    private Integer finishNum;
    
    private Integer onTheWayNum;
    
    private Integer allNum;
    
    /** 
    * 获取新增订单数
    * @return
    */
    public Integer getNewNum()
    {
        return newNum;
    }
    
    /** 
    * 设置新增订单数
    * @param newNum
    */
    public void setNewNum(Integer newNum)
    {
        this.newNum = newNum;
    }
    
    /** 
    * 获取办结订单数
    * @return
    */
    public Integer getFinishNum()
    {
        return finishNum;
    }
    
    /** 
    * 设置办结订单数
    * @param newNum
    */
    public void setFinishNum(Integer finishNum)
    {
        this.finishNum = finishNum;
    }
    
    /** 
    * 获取在途订单数
    * @return
    */
    public Integer getOnTheWayNum()
    {
        return onTheWayNum;
    }
    
    /** 
    * 设置在途订单数
    * @param newNum
    */
    public void setOnTheWayNum(Integer onTheWayNum)
    {
        this.onTheWayNum = onTheWayNum;
    }
    
    /** 
    * 获取总订单数
    * @return
    */
    public Integer getAllNum()
    {
        return allNum;
    }
    
    /** 
    * 设置总订单数
    * @param newNum
    */
    public void setAllNum(Integer allNum)
    {
        this.allNum = allNum;
    }
}
