package cn.com.eju.deal.contract.model;

import java.util.Date;

/**
 * Created by haidan on 2018/11/13.
 */
public class OaContractChangeReturn {
    private Integer id;

    /**
     * CX：撤销；BG：变更；ZZ：终止
     */
    private String changeType;

    private String oaNo;

    private String flowId;

    /**
     * 1:通过；1以外：不通过
     */
    private Integer approveStatus;

    private Date approveDate;

    private Integer hasDeal;

    private String dealDesc;

    private Date dateUpdate;

    private Date dateCreate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getChangeType() {
        return changeType;
    }

    public void setChangeType(String changeType) {
        this.changeType = changeType;
    }

    public String getOaNo() {
        return oaNo;
    }

    public void setOaNo(String oaNo) {
        this.oaNo = oaNo;
    }

    public String getFlowId() {
        return flowId;
    }

    public void setFlowId(String flowId) {
        this.flowId = flowId;
    }

    public Integer getApproveStatus() {
        return approveStatus;
    }

    public void setApproveStatus(Integer approveStatus) {
        this.approveStatus = approveStatus;
    }

    public Date getApproveDate() {
        return approveDate;
    }

    public void setApproveDate(Date approveDate) {
        this.approveDate = approveDate;
    }

    public Integer getHasDeal() {
        return hasDeal;
    }

    public void setHasDeal(Integer hasDeal) {
        this.hasDeal = hasDeal;
    }

    public String getDealDesc() {
        return dealDesc;
    }

    public void setDealDesc(String dealDesc) {
        this.dealDesc = dealDesc;
    }

    public Date getDateUpdate() {
        return dateUpdate;
    }

    public void setDateUpdate(Date dateUpdate) {
        this.dateUpdate = dateUpdate;
    }

    public Date getDateCreate() {
        return dateCreate;
    }

    public void setDateCreate(Date dateCreate) {
        this.dateCreate = dateCreate;
    }
}
