package cn.com.eju.deal.houseLinkage.estate.model;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

public class EstateDyRecord implements Serializable{

	private Integer id;
	private String estateId;
	private String projectNo;
	private Date dyStartDate;
	private Date dyEndDate;
	private BigDecimal dyAmount;
	private Date crtDt;
	private Boolean delFlg;
	private String dyscale;//比例

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getEstateId() {
		return estateId;
	}

	public void setEstateId(String estateId) {
		this.estateId = estateId;
	}

	public String getProjectNo() {
		return projectNo;
	}

	public void setProjectNo(String projectNo) {
		this.projectNo = projectNo;
	}

	public Date getDyStartDate() {
		return dyStartDate;
	}

	public void setDyStartDate(Date dyStartDate) {
		this.dyStartDate = dyStartDate;
	}

	public Date getDyEndDate() {
		return dyEndDate;
	}

	public void setDyEndDate(Date dyEndDate) {
		this.dyEndDate = dyEndDate;
	}

	public BigDecimal getDyAmount() {
		return dyAmount;
	}

	public void setDyAmount(BigDecimal dyAmount) {
		this.dyAmount = dyAmount;
	}

	public Date getCrtDt() {
		return crtDt;
	}

	public void setCrtDt(Date crtDt) {
		this.crtDt = crtDt;
	}

	public Boolean getDelFlg() {
		return delFlg;
	}

	public void setDelFlg(Boolean delFlg) {
		this.delFlg = delFlg;
	}

	public String getDyscale() {
		return dyscale;
	}

	public void setDyscale(String dyscale) {
		this.dyscale = dyscale;
	}
}